# This is a simple Ruby script

# Define a method to greet the user
def greet(name)
  puts "Hello, #{name}!"
end

# Call the method with a sample name
greet("World")
# Define a method to add two numbers
def add(a, b)
  return a + b
end

# Call the add method with sample numbers
sum = add(5, 3)
puts "The sum of 5 and 3 is #{sum}"

# Define a method to subtract two numbers
def subtract(a, b)
  return a - b
end

# Call the subtract method with sample numbers
difference = subtract(10, 4)
puts "The difference between 10 and 4 is #{difference}"

